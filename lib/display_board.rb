# Define display_board that accepts a board and prints
# out the current state.
def display_board(board)
  print " #{board[1]} | #{board[2]} | #{board[3]} ";
  puts "-----------";

  print " #{board[4]} | #{board[5]} | #{board[6]} ";
  puts "-----------";

  print " #{board[7]} | #{board[8]} | #{board[9]} ";
end
